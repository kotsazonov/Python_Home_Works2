# Задача 30:  Заполните массив элементами арифметической прогрессии
# . Её первый элемент, разность и количество элементов нужно ввести с клавиатуры.
# Формула для получения n-го члена прогрессии: an = a1 + (n-1) * d.
# Каждое число вводится с новой строки.
def create_arithmetic_progression(a1, d, n):
    """
    Создает арифметическую прогрессию на основе введенных данных.
    a1: первый элемент
    d: разность
    n: количество элементов
    """
    return [a1 + (i - 1) * d for i in range(1, n + 1)]

def main():
    # Ввод данных
    a1 = float(input("Введите первый элемент прогрессии: "))
    d = float(input("Введите разность прогрессии: "))
    n = int(input("Введите количество элементов прогрессии: "))

    # Создание прогрессии и вывод результатов
    progression = create_arithmetic_progression(a1, d, n)
    print("Арифметическая прогрессия:")
    print(progression)

if __name__ == "__main__":
    main()

# Задача 22: Даны два неупорядоченных
# набора целых чисел (может быть, с повторениями). Выдать без
# повторений в порядке
# возрастания все те числа, которые встречаются в обоих наборах.
# Пользователь вводит 2 числа. n — кол-во элементов
# первого множества. m — кол-во элементов второго множества.
# Затем пользователь вводит сами элементы множеств.

def intersection_of_sets(n, m):
    # Создаем два пустых множества
    set1 = set()
    set2 = set()

    # Вводим элементы первого множества
    print("Введите элементы первого множества:")
    for i in range(n):
        element = int(input())
        set1.add(element)

    # Вводим элементы второго множества
    print("Введите элементы второго множества:")
    for i in range(m):
        element = int(input())
        set2.add(element)

    # Вычисляем пересечение множеств
    intersection = set1.intersection(set2)

    # Выводим элементы пересечения в порядке возрастания
    print("Числа, которые встречаются в обоих множествах:")
    for element in sorted(intersection):
        print(element)

# Вводим количество элементов для каждого множества
n = int(input("Введите количество элементов первого множества: "))
m = int(input("Введите количество элементов второго множества: "))

# Вызываем функцию для выполнения программы
intersection_of_sets(n, m)

